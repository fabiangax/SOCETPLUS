    private void AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarActionPerformed
        String fecha = String.valueOf(fechaSeg.getCalendar().get(Calendar.DAY_OF_MONTH)+"/"+(fechaSeg.getCalendar().get(Calendar.MONTH)+1)+"/"+fechaSeg.getCalendar().get(Calendar.YEAR));
        if(check3.isVisible() && check4.isVisible() && check5.isVisible()){
            int res = JOptionPane.showConfirmDialog(null, "¿Confirmar registro?", "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if(res==0){
                ResultSet resultado = buscar(txtMatricula.getText());
                try{
                    if(resultado.next()){
                        JOptionPane.showMessageDialog(this, "La matricula ya existe","Aviso",JOptionPane.INFORMATION_MESSAGE);
                    }else{
                        insertar(txtMatricula.getText(),fecha,txtPago.getText(),txtADescripcion.getText(),obraSS);
                        Utilidades uti = new Utilidades();
                        uti.clickEnObra();
                        JOptionPane.showMessageDialog(null, "Registro de seguimiento completado correctamente", "Registro exitoso", JOptionPane.DEFAULT_OPTION);
                        this.dispose(); 
                    }
                }catch(SQLException e){
                    System.out.println(e);
                }
            }   
        }
        else{
            if(txtMatricula.getText().isEmpty() || txtPago.getText().isEmpty() || txtADescripcion.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Hay campos vacíos. Favor de llenar los campos faltantes y vuelva a intentarlo.", "Campos vacíos", JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                JOptionPane.showMessageDialog(null, "Hay campos no válidos. Favor de revisar la información y velva a intentarlo", "Campos inválidos", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_AceptarActionPerformed

