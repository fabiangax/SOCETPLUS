    public void insertar(String matricula, String fecha, String Pago, String Descripcion, String Obra){
        try{
           Statement ST = c.createStatement();
           String sql = "Insert into Seguimientos values('"+matricula
                    +"','"+fecha+"','"+Pago
                    +"','"+Descripcion+"','"+Obra+"')";
           ST.executeUpdate(sql);
        }catch(SQLException e){
            System.out.println(e);
        }
    }
    public ResultSet buscar(String str){
        try{
           Statement ST = c.createStatement();
           String sql = "Select * from Seguimientos where IDSeguimiento='"+str+"'";
           return ST.executeQuery(sql);
        }catch(SQLException e){
            System.out.println(e);
        }
        return null;
    }
    
